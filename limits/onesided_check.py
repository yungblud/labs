"""좌·우극한 관찰 (파트 1 보조) — 순수 표준 라이브러리.

예: f(x) = |x| / x  (x≠0).  x→0 에서 좌극한/우극한이 다른 대표 예.
왼쪽(x<0)에서 다가가면 −1, 오른쪽(x>0)에서 다가가면 +1 → 좌≠우 → 극한 없음.
"""


def f(x):
    return abs(x) / x  # x>0 → 1,  x<0 → −1


if __name__ == "__main__":
    print("f(x) = |x|/x 의 x→0 좌·우극한\n")
    print(f"{'x (왼쪽에서)':>14} | {'f(x)':>6}     {'x (오른쪽에서)':>14} | {'f(x)':>6}")
    print("-" * 56)
    for k in range(1, 7):
        d = 10.0 ** (-k)
        left = f(-d)   # 0 보다 살짝 작은 쪽에서 접근
        right = f(d)   # 0 보다 살짝 큰 쪽에서 접근
        print(f"{-d:>14.0e} | {left:>6.0f}     {d:>14.0e} | {right:>6.0f}")
    print("\n좌극한 = −1,  우극한 = +1  →  서로 다름  →  lim_{x→0} 는 존재하지 않음")
